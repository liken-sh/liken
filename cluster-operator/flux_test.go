func TestEnsureFluxEnginePlantsWhenAbsent(t *testing.T) {
	var created []string
	c := testClient(t, http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		switch r.Method {
		case http.MethodGet:
			http.NotFound(w, r)
		case http.MethodPost:
			created = append(created, r.URL.Path)
			// The namespace already exists (liken owns it); the
			// planter must shrug and continue.
			if r.URL.Path == "/api/v1/namespaces" {
				w.WriteHeader(http.StatusConflict)
				return
			}
			w.WriteHeader(http.StatusCreated)
		}
	}))
	ensureFluxEngine(c, fluxCluster(), []byte(testSeed))
	want := []string{
		"/api/v1/namespaces",
		"/apis/apiextensions.k8s.io/v1/customresourcedefinitions",
		"/apis/apps/v1/namespaces/flux-system/deployments",
	}
	if !slices.Equal(created, want) {
		t.Errorf("created %v, want %v", created, want)
	}
}

